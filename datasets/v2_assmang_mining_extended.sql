-- ========================================================================
-- FACT TABLE: Monthly Operating Costs
-- ========================================================================
CREATE TABLE FactOperatingCosts (
    CostID INT PRIMARY KEY IDENTITY(1,1),
    MineID INT NOT NULL FOREIGN KEY REFERENCES Dim_Mine(MineID),
    DepartmentID INT NOT NULL FOREIGN KEY REFERENCES Dim_Department(DepartmentID),
    DateID INT NOT NULL FOREIGN KEY REFERENCES Dim_Date(DateID),
    LaborCostZAR DECIMAL(12,2),
    EquipmentCostZAR DECIMAL(12,2),
    MaintenanceCostZAR DECIMAL(12,2),
    SafetyCostZAR DECIMAL(12,2),
    UtilitiesCostZAR DECIMAL(12,2),
    OtherCostZAR DECIMAL(12,2),
    CreatedDate DATETIME DEFAULT GETDATE()
);

