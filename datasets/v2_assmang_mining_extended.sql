-- ========================================================================
-- FACT TABLE: Monthly Production
-- ========================================================================
-- Records monthly mineral production by mine
-- Updated monthly, slowly-changing (SCD Type 1)
-- ========================================================================

CREATE TABLE FactProduction (
    ProductionID INT PRIMARY KEY IDENTITY(1,1),
    MineID INT NOT NULL FOREIGN KEY REFERENCES Dim_Mine(MineID),
    DateID INT NOT NULL FOREIGN KEY REFERENCES Dim_Date(DateID),
    TonnesProduced DECIMAL(15,2),  -- Total tonnes
    Grade DECIMAL(5,2),             -- Quality percentage (Fe%, Mn%, Cr%)
    RevenueZAR DECIMAL(15,2),       -- Revenue in ZAR
    CostPerTonneZAR DECIMAL(10,2),  -- Unit cost
    CreatedDate DATETIME DEFAULT GETDATE()
);

